    public void Searchfetch(String websiteName) {
        String search = websiteName;
        try {
            DbClass.createCon();
            String query = "SELECT websitename as 'Website Name', username as 'Username', email as 'Email', password as 'Password', url as 'URL', description as 'Description' FROM StoreAccountPassword where websitename = '" + search + "' and registration_Id = " + userId;
            DbClass.pst = DbClass.con.prepareStatement(query);
            DbClass.rs = DbClass.pst.executeQuery();
            AccountTable.setModel(DbUtils.resultSetToTableModel(DbClass.rs));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error In Search Fetch", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

