    void adminChecker() {
        try {
            DbClass.createCon();
            String query = "SELECT username FROM Registration where Id = " + userId;
            DbClass.pst = DbClass.con.prepareStatement(query);
            DbClass.rs = DbClass.pst.executeQuery();
            while (DbClass.rs.next()) {
                String uname = DbClass.rs.getString(1);
                if (uname.equals("admin")) {
                    btnDeleteMember.setVisible(true);
                } else if (!uname.equals("admin")) {
                    btnDeleteMember.setVisible(false);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Admin Not Found");
        }
    }

