    private void btnAddAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddAccountActionPerformed
        if (txtWebsiteName.getText().equals("") || txtUsername.getText().equals("") || txtEmail.getText().equals("") || txtPassword.getText().equals("") || txtURL.getText().equals("") || txtDescription.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All fields are required");
        } else {
            try {
                DbClass.createCon();
                String query = "insert into StoreAccountPassword (Registration_Id, WebsiteName, Username, Email, Password, URL, Description) values(?,?,?,?,?,?,?)";
                DbClass.pst = DbClass.con.prepareStatement(query);

                DbClass.pst.setInt(1, userId);
                DbClass.pst.setString(2, txtWebsiteName.getText());
                DbClass.pst.setString(3, txtUsername.getText());
                DbClass.pst.setString(4, txtEmail.getText());
                DbClass.pst.setString(5, txtPassword.getText());
                DbClass.pst.setString(6, txtURL.getText());
                DbClass.pst.setString(7, txtDescription.getText());

                DbClass.pst.executeUpdate();
                AccountTableFetch();
                JOptionPane.showMessageDialog(this, "Account Added Successfully", "Status", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Account Didn't Added", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAddAccountActionPerformed

